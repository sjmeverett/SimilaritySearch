package ndi;

/**
 * @author stewart
 */
public class ImagePair {
    private int image1, image2;


    public ImagePair(int image1, int image2) {
        this.image1 = image1;
        this.image2 = image2;
    }


    public int getImage1() {
        return image1;
    }


    public int getImage2() {
        return image2;
    }
}
